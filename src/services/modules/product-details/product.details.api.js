import axios from 'axios';

const APIV1 = 'http://pos.localtest.me/api/admin/product-details/'

export const CreateProductDetails = async (reqBody, token) => {
    return await axios({ 
        headers: {
            'Content-Type': 'application/json',
            'Authorization': `Bearer ${token}`
        },
        method: 'POST', 
        url: `${APIV1}create`, 
        data: reqBody
    });
}

